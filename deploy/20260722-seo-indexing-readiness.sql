SET NOCOUNT ON;

DECLARE @Now datetimeoffset = SYSDATETIMEOFFSET();

UPDATE tags
SET
    [Description] = updates.[Description],
    [UpdatedAt] = @Now
FROM [Tags] AS tags
INNER JOIN (VALUES
    (N'enterprise-ai', N'راهنماها، محصولات و سناریوهای عملی برای طراحی، استقرار و مدیریت هوش مصنوعی در سازمان‌های ایرانی.'),
    (N'ai-agent', N'طراحی ایجنت‌های تخصصی و دستیارهای هوشمند برای اجرای وظایف سازمانی، اتصال به داده و خودکارسازی فرایندها.'),
    (N'banking-finance', N'کاربردهای هوش مصنوعی در بانک، بیمه و خدمات مالی؛ از تحلیل داده و ریسک تا پاسخ‌گویی هوشمند و کنترل عملیات.'),
    (N'data-analysis', N'تحلیل داده، داشبورد مدیریتی، پرسش فارسی از داده و تبدیل گزارش‌های سازمانی به تصمیم قابل اقدام.'),
    (N'data-governance', N'موضوعات مرتبط با امنیت داده، کنترل دسترسی، اعتماد سازمانی، audit و سیاست‌گذاری استفاده از AI در سازمان.'),
    (N'intelligent-automation', N'طراحی و اجرای جریان‌های کاری هوشمند برای کاهش کار دستی، اتصال سیستم‌ها و افزایش سرعت عملیات سازمانی.'),
    (N'language-models', N'مقاله‌ها و راهکارهای مرتبط با انتخاب، استقرار، ارزیابی و بهره‌برداری از مدل‌های زبانی در محیط سازمانی.'),
    (N'llmops', N'مدیریت عملیاتی مدل‌های زبانی شامل پایش کیفیت، کنترل هزینه، لاگ، ارزیابی، امنیت و آماده‌سازی سرویس پایدار سازمانی.'),
    (N'private-ai', N'استقرار کنترل‌شده هوش مصنوعی در زیرساخت اختصاصی سازمان با تمرکز بر حریم داده، دسترسی، امنیت و بهره‌برداری قابل اعتماد.'),
    (N'voice-of-customer', N'تحلیل شکایت، بازخورد و تجربه مشتریان با هوش مصنوعی برای ریشه‌یابی مسئله، پایش SLA و بهبود خدمات.')
) AS updates([Slug], [Description])
    ON updates.[Slug] = tags.[Slug]
WHERE LEN(LTRIM(RTRIM(tags.[Description]))) < 50;
