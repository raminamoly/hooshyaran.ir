    private static async Task AttachTagsToArticleAsync(HooshyaranDbContext dbContext, string articleSlug, params string[] tagSlugs)
    {
        var article = await dbContext.BlogArticles.SingleOrDefaultAsync(item => item.Slug == articleSlug);
        if (article is null)
        {
            return;
        }

        var tagIds = await dbContext.Tags
            .Where(tag => tagSlugs.Contains(tag.Slug))
            .Select(tag => tag.Id)
            .ToListAsync();

        foreach (var tagId in tagIds)
        {
            if (!await dbContext.BlogArticleTags.AnyAsync(item => item.BlogArticleId == article.Id && item.TagId == tagId))
            {
                dbContext.BlogArticleTags.Add(new BlogArticleTag { BlogArticleId = article.Id, TagId = tagId });
            }
        }
    }

    private static async Task AttachTagsToProductAsync(HooshyaranDbContext dbContext, string productSlug, params string[] tagSlugs)
    {
