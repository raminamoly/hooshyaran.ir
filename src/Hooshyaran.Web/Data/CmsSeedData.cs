            .Where(tag => tagSlugs.Contains(tag.Slug))
            .Select(tag => tag.Id)
            .ToListAsync();

        foreach (var tagId in tagIds)
        {
            if (!await dbContext.ProductTags.AnyAsync(item => item.ProductId == product.Id && item.TagId == tagId))
            {
                dbContext.ProductTags.Add(new ProductTag { ProductId = product.Id, TagId = tagId });
            }
        }
    }

